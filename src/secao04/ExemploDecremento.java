package secao04;

public class ExemploDecremento {
    public static void main(String[] args) {
        int saldoConta = 100;
        int totalConta = saldoConta--;
        System.out.println(saldoConta);
        System.out.println(totalConta);
        System.out.println();

        System.out.println("Exemplo 2");
        int saldoConta1 = 100;
        int totalConta1 = --saldoConta1;
        System.out.println(saldoConta1);
        System.out.println(totalConta1);


    }
}
